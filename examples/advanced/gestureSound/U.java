package advanced.gestureSound;

public class U {
	public static int minmax(int val, int lower, int upper) {
		return Math.max(lower, Math.min(val, upper));
	}
	public static double minmax(double val, double lower, double upper) {
		return Math.max(lower, Math.min(val, upper));
	}
	public static float minmax(float val, float lower, float upper) {
		return Math.max(lower, Math.min(val, upper));
	}

}
